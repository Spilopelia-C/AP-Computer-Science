public class WellFormed {
    public static void main(String[] args) {
        System.out.println();
        System.out.print("A well-formed Java program has ");
        System.out.print("a main method with { and } ");
        System.out.println("braces.");
        System.out.println();
        System.out.print("A System.out.println statement ");
        System.out.print("has ( and ) and usually a ");
        System.out.print("String that starts and ends with a \" character. ");
        System.out.println("(But we type \\\" instead!)");
        System.out.println();
    }
}